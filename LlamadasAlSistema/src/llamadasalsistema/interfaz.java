    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: 
        btncrear.setEnabled(false);
        int x=0;
        try{
            x = Integer.parseInt(JOptionPane.showInputDialog("Numero de palabras: "));
        }
        catch(Exception e) {//Captura la exeception de que no exista  el archivo que se lea
      JOptionPane.showMessageDialog(null,"Ingrese un numero");  
      
    }
    try {
         try (FileWriter fr = new FileWriter(this.fr,true)) {
             BufferedWriter br = new BufferedWriter(fr);//se crea un buffer del tama√±o del archivo
             PrintWriter pw = new  PrintWriter (br);
            for(int i =0; i<x;i++)
            {
             String n = JOptionPane.showInputDialog("PALABRA: ");
             pw.println(n);
             
            }
               br.close();   
              
         }     
    }
    catch(Exception e) {//Captura la exeception de que no exista  el archivo que se lea
      JOptionPane.showMessageDialog(null,"El archivo no existe");
      
    }
     JOptionPane.showMessageDialog(null,"Se ha terminado correctamente");
    
    }//GEN-LAST:event_jButton1ActionPerformed

